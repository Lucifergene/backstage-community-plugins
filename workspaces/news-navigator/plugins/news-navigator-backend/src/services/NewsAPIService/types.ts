export interface NewsItem {
  source: {
    id: string;
    name: string;
  };
  author: string;
  title: string;
  description: string;
  url: string;
  urlToImage: string;
  publishedAt: string;
  content: string;
}

export interface NewsAPIResponse {
  status: string;
  totalResults: number;
  articles: NewsItem[];
}

export interface NewsAPIService {
  getNewsByCategory(category: string): Promise<NewsItem[]>;
  searchNewsByKeyword(keyword: string): Promise<NewsItem[]>;
}
